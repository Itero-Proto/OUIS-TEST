function shuffleArray(array) {
    for (let i = array.length - 1; i > 0; i--) {
        const j = Math.floor(Math.random() * (i + 1));
        [array[i], array[j]] = [array[j], array[i]];
    }
}

function checkAnswers() {
    const q = questions[currentQuestionIndex];
    const checkboxes = answersEl.querySelectorAll('input[type="checkbox"]');

    // –º–∞—Å—Å–∏–≤ –≤—ã–±—Ä–∞–Ω–Ω—ã—Ö –∏–Ω–¥–µ–∫—Å–æ–≤
    const selected = Array.from(checkboxes)
        .map((cb, i) => cb.checked ? i : -1)
        .filter(i => i !== -1);

    // –º–∞—Å—Å–∏–≤ –ø—Ä–∞–≤–∏–ª—å–Ω—ã—Ö –∏–Ω–¥–µ–∫—Å–æ–≤
    const correctIndices = q.answers
        .map((a, i) => a.correct ? i : -1)
        .filter(i => i !== -1);

    // –ø–æ–¥—Å–≤–µ—Ç–∫–∞ –ø—Ä–∞–≤–∏–ª—å–Ω—ã—Ö –∏ –Ω–µ–ø—Ä–∞–≤–∏–ª—å–Ω—ã—Ö –≤–∞—Ä–∏–∞–Ω—Ç–æ–≤
    checkboxes.forEach((cb, i) => {
        cb.disabled = true;
        if (q.answers[i].correct) cb.parentElement.classList.add('correct');
        if (cb.checked && !q.answers[i].correct) cb.parentElement.classList.add('wrong');
    });

    // –Ω–∞—á–∏—Å–ª—è–µ–º 1 –±–∞–ª–ª —Ç–æ–ª—å–∫–æ –µ—Å–ª–∏ –≤—ã–±—Ä–∞–Ω **–≤—Å–µ –ø—Ä–∞–≤–∏–ª—å–Ω—ã–µ –∏ —Ç–æ–ª—å–∫–æ –æ–Ω–∏**
    const allCorrect = selected.length === correctIndices.length &&
                       selected.every(i => correctIndices.includes(i));
    if (allCorrect) score++;

    nextBtn.disabled = false;
}

function selectAnswer(btn, correct) {
    const buttons = answersEl.querySelectorAll('button');
    buttons.forEach((b, index) => {
        b.disabled = true;
        if (questions[currentQuestionIndex].answers[index].correct) {
            b.classList.add('correct'); // –≤—Å–µ–≥–¥–∞ –ø–æ–¥—Å–≤–µ—á–∏–≤–∞–µ–º –ø—Ä–∞–≤–∏–ª—å–Ω—ã–π –æ—Ç–≤–µ—Ç
        }
    });

    if (!correct) {
        btn.classList.add('wrong'); // –ø–æ–¥—Å–≤–µ—á–∏–≤–∞–µ–º –≤—ã–±—Ä–∞–Ω–Ω—ã–π –Ω–µ–ø—Ä–∞–≤–∏–ª—å–Ω—ã–π –æ—Ç–≤–µ—Ç
    } else {
        score++;
    }

    nextBtn.disabled = false;
}

